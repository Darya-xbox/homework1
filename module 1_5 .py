immutable_var = 1, 2, 3, True, 'a'
print(immutable_var)
# immutable_var[0][0] = 4, кортежи неизменяемы, что означает, что вы не можете изменять, добавлять или удалять элементы после создания кортежа
mutable_list = [1, 2, 44, 45, 'a']
print(mutable_list)
mutable_list [2] = 20
print(mutable_list)
